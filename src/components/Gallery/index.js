class Gallery extends Component {
  state = {
    primeImageId: imagesList[0].id,
  }

  changePrimeImage = id => {
    this.setState({
      primeImageId: id,
    })
  }

  render() {
    const {primeImageId} = this.state
    const imageObject = imagesList.filter(
      eachItem => eachItem.id === primeImageId,
    )
    return (
      <div className="bg-container">
        <div className="first">
          <img
            alt={imageObject[0].imageAltText}
            className="firstImage"
            src={imageObject[0].imageUrl}
          />
        </div>
        <div className="second">
          <h1>Nature Photography</h1>
          <p>Nature Photography by rahul</p>
          <ul className="inner">
            {imagesList.map(eachObject => (
              <ThumbnailItem
                details={eachObject}
                key={eachObject.id}
                primeImageId={primeImageId}
                changePrimeImage={this.changePrimeImage}
              />
            ))}
          </ul>
        </div>
      </div>
    )
  }
}

export default Gallery
